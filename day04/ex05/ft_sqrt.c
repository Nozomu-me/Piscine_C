#include <stdio.h>

int		ft_sqrt(int nb)
{
	long int		i;

	i = 0;
	while(i * i < nb)
		i++;
	if(i * i == nb)
		return (i);
	return (0);
}
int		main(void)
{
	printf("%d", ft_sqrt(2147483647));

}
